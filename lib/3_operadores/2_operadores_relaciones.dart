void main() {
  //Operadores Relacionais
  // ==, !=, <, >, <=, >=
  // == é o operador de igualdade
  // != é o operador de diferença
  // < é o operador de menor que
  // > é o operador de maior que
  // <= é o operador de menor ou igual
  // >= é o operador de maior ou igual

  final idade = 18;

  if (idade >= 18) {
    print("Maior de idade");
  } else {
    print("Menor de idade");
  }

}
